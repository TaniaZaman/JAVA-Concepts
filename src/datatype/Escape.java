package datatype;

public class Escape {
	
	public static void main(String[] args) {
		
		
//		escape is a special character is followed by backslash;
//		/t = tab
//		/b= backspace
	/* /n= new line
	 * /r=carriage return
	 * 	/" = double quotation
	 *  /'=single quotation		
	 */ //= backslash
		
		
		System.out.println("1 \t 2 \r3 \t 4");
		System.out.println("\"java Programming\"");
		System.out.println("java \r");
		System.out.println("\'java\' \t basic\b java ");


	}

}
